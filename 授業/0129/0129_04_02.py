# リストの初期化と参照
# リストの初期化
# 干支の名前をリスト型でまとめる
eto_list = ['子', '丑', '寅', '卯', '辰', '巳',
'午', '未', '申', '酉', '戌', '亥']
# リスト内のデータを参照
# リストの[2]のデータを変数 name に代入
eto_name = eto_list[2]
print('あなたの干支は', eto_name, 'です。')
