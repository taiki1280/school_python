// sum()関数の実体
int
sum(int a, int b)
{
    int return_value;

    return_value = a + b;

    return return_value;
}

// sub()関数の実体
int
sub(int a, int b)
{
    int return_value;

    return_value = a - b;

    return return_value;
}

// mul()関数の実体
int
mul(int a, int b)
{
    int return_value;

    return_value = a * b;

    return return_value;
}

// div()関数の実体
int
div(int a, int b)
{
    int return_value;

    return_value = a / b;

    return return_value;
}
