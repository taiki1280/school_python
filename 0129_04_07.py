# 関数の具体例

# int()関数に文字列'10'を引数として渡して呼び出し
# number1変数に整数値を返す
number1 = int('10')
# int()関数に文字列'2017'を引数として渡して呼び出し
# number2変数に整数値を返す
number2 = int('2017')
# int()関数に文字列'-100'を引数として渡して呼び出し
# number3変数に整数値を返す
number3 = int('-100')

# number1, number2, number3の表示
print(number1, number2, number3, sep = ',')