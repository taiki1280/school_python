# Hello を指定された回数表示する関数
# 関数 hello_times(times) の定義


def hello_times(times):
    # 指定された回数 Hello を表示する
    # for count in ___ア___:
    for count in range(times):
        print('Hello')


print('３回 Hello を表示する')
# hello_times を呼び出す（引数 3）
# ___イ___
hello_times(3)
print('５回 Hello を表示する')
# ___ウ___
hello_times(5)
